  /// Function yo guhindura amajambo
  String t(String key) {
    // Rondera ijambo mu rurimi ruriko rurakoreshwa
    final currentMap = _localizedStrings[_currentLanguage];
    if (currentMap != null && currentMap.containsKey(key)) {
      return currentMap[key]!;
    }
    // Iyo ritabonetse, rondera mu Kirundi (fallback)
    final fallbackMap = _localizedStrings['ki'];
    if (fallbackMap != null && fallbackMap.containsKey(key)) {
      return fallbackMap[key]!;
    }
    // Iyo ritabonetse na hamwe, subiza "key" ubwayo
    return key;
  }

  /// Function yo guhindura ururimi
  Future<void> changeLanguage(String code) async {
    _currentLanguage = code;
    notifyListeners(); // Bwira porogarama yose ko ururimi rwahindutse
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString('languageCode', code);
  }

  /// Function yo gukurura ururimi rwabitswe
  Future<void> _loadLanguage() async {
    final prefs = await SharedPreferences.getInstance();
    String? savedLang = prefs.getString('languageCode');
    if (savedLang == null) {
      _currentLanguage = 'ki'; // Ururimi rwa mbere ni Ikirundi
    } else {
      _currentLanguage = savedLang;
    }
    notifyListeners();
  }
